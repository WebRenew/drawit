// Get all colors for a specific scale (for the expanded picker)
export const getColorScale = (scaleName: keyof typeof GEIST_COLORS, isDark: boolean) => {
  const scale = GEIST_COLORS[scaleName]
  return isDark ? scale.dark : scale.light
}

